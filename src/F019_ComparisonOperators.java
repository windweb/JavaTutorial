// Comparison Operators / Операторы сравнения

public class F019_ComparisonOperators {
    public static void main(String[] args) {
        int x = 1;
        int y = 1;
        // cравним числа
          System.out.println(x < y);
          System.out.println(x > y);
          System.out.println(x <= y);
          System.out.println(x >= y);
          System.out.println(x == y);
          System.out.println(x != y);

    }
}
